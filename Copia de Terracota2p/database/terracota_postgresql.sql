CREATE OR REPLACE FUNCTION autenticar_usuario(p_usuario text, p_password text)
RETURNS TABLE(usuario_id bigint, nombre varchar, usuario varchar, roles text[])
LANGUAGE sql
STABLE
SET search_path = terracota, public
AS $$
  SELECT u.id, u.nombre, u.usuario,
         array_agg(r.clave ORDER BY r.clave)::text[]
  FROM usuarios u
  JOIN usuario_roles ur ON ur.usuario_id = u.id
  JOIN roles r ON r.id = ur.rol_id
  WHERE u.activo
    AND lower(u.usuario) = lower(btrim(p_usuario))
    AND u.password_hash = crypt(p_password, u.password_hash)
  GROUP BY u.id, u.nombre, u.usuario;
$$;

DROP FUNCTION IF EXISTS crear_pedido(smallint, bigint, jsonb, varchar, numeric);
CREATE OR REPLACE FUNCTION crear_pedido(
  p_numero_mesa integer,
  p_mesero_id bigint,
  p_items jsonb,
  p_notas varchar DEFAULT NULL,
  p_tasa_impuesto numeric DEFAULT 0.1600
)
RETURNS pedidos
LANGUAGE plpgsql
SET search_path = terracota, public
AS $$
DECLARE
  mesa_registro mesas%ROWTYPE;
  pedido_nuevo pedidos%ROWTYPE;
BEGIN
  IF jsonb_typeof(p_items) <> 'array' OR jsonb_array_length(p_items) = 0 THEN
    RAISE EXCEPTION 'El pedido debe contener al menos un producto.' USING ERRCODE = 'check_violation';
  END IF;

  IF p_tasa_impuesto < 0 OR p_tasa_impuesto > 1 THEN
    RAISE EXCEPTION 'La tasa de impuesto no es válida.' USING ERRCODE = 'check_violation';
  END IF;

  IF NOT EXISTS (
    SELECT 1 FROM usuarios u
    JOIN usuario_roles ur ON ur.usuario_id = u.id
    JOIN roles r ON r.id = ur.rol_id
    WHERE u.id = p_mesero_id AND u.activo AND r.clave IN ('mesero', 'administrador')
  ) THEN
    RAISE EXCEPTION 'El usuario no tiene permisos de mesero.' USING ERRCODE = 'insufficient_privilege';
  END IF;

  SELECT * INTO mesa_registro FROM mesas
  WHERE numero = p_numero_mesa AND activa
  FOR UPDATE;

  IF NOT FOUND THEN RAISE EXCEPTION 'La mesa % no existe o está inactiva.', p_numero_mesa; END IF;
  IF mesa_registro.estado <> 'DISPONIBLE' THEN
    RAISE EXCEPTION 'La mesa % no está disponible.', p_numero_mesa USING ERRCODE = 'unique_violation';
  END IF;

  IF EXISTS (
    SELECT 1 FROM jsonb_array_elements(p_items) item
    WHERE NOT (item ? 'producto_clave')
       OR NOT (item ? 'cantidad')
       OR COALESCE(item->>'cantidad', '') !~ '^[1-9][0-9]*$'
       OR (item->>'cantidad')::integer > 99
  ) THEN
    RAISE EXCEPTION 'Uno o más productos tienen un formato inválido.' USING ERRCODE = 'invalid_parameter_value';
  END IF;

  IF EXISTS (
    SELECT 1 FROM jsonb_array_elements(p_items) item
    LEFT JOIN productos p ON p.clave = item->>'producto_clave' AND p.disponible
    WHERE p.id IS NULL
  ) THEN
    RAISE EXCEPTION 'Uno o más productos no existen o no están disponibles.';
  END IF;

  INSERT INTO pedidos(mesa_id, mesero_id, tasa_impuesto, notas, actualizado_por)
  VALUES (mesa_registro.id, p_mesero_id, p_tasa_impuesto, NULLIF(btrim(p_notas), ''), p_mesero_id)
  RETURNING * INTO pedido_nuevo;

  INSERT INTO pedido_detalles(
    pedido_id, producto_id, nombre_producto, cantidad, precio_unitario, observacion
  )
  SELECT pedido_nuevo.id, p.id, p.nombre, (item->>'cantidad')::integer, p.precio,
         NULLIF(btrim(item->>'observacion'), '')
  FROM jsonb_array_elements(p_items) item
  JOIN productos p ON p.clave = item->>'producto_clave' AND p.disponible;

  SELECT * INTO pedido_nuevo FROM pedidos WHERE id = pedido_nuevo.id;
  RETURN pedido_nuevo;
END;
$$;

CREATE OR REPLACE FUNCTION cambiar_estado_pedido(
  p_pedido_id bigint,
  p_estado varchar,
  p_usuario_id bigint,
  p_comentario varchar DEFAULT NULL
)
RETURNS pedidos
LANGUAGE plpgsql
SET search_path = terracota, public
AS $$
DECLARE
  resultado pedidos%ROWTYPE;
BEGIN
  IF NOT EXISTS (SELECT 1 FROM usuarios WHERE id = p_usuario_id AND activo) THEN
    RAISE EXCEPTION 'Usuario inválido o inactivo.';
  END IF;

  UPDATE pedidos
     SET estado = upper(p_estado), actualizado_por = p_usuario_id
   WHERE id = p_pedido_id
   RETURNING * INTO resultado;

  IF NOT FOUND THEN RAISE EXCEPTION 'El pedido % no existe.', p_pedido_id; END IF;

  IF NULLIF(btrim(p_comentario), '') IS NOT NULL THEN
    UPDATE pedido_estados_historial
       SET comentario = left(btrim(p_comentario), 250)
     WHERE id = (
       SELECT id FROM pedido_estados_historial
       WHERE pedido_id = p_pedido_id ORDER BY creado_en DESC, id DESC LIMIT 1
     );
  END IF;
  RETURN resultado;
END;
$$;

CREATE OR REPLACE FUNCTION registrar_pago(
  p_pedido_id bigint,
  p_cajero_id bigint,
  p_metodo varchar,
  p_monto_recibido numeric DEFAULT NULL,
  p_referencia varchar DEFAULT NULL
)
RETURNS TABLE(ticket_id bigint, folio varchar, total numeric, cambio numeric)
LANGUAGE plpgsql
SET search_path = terracota, public
AS $$
DECLARE
  pedido_registro pedidos%ROWTYPE;
  pago_id_nuevo bigint;
  ticket_id_nuevo bigint;
  folio_nuevo varchar(12);
  metodo_normalizado varchar(20) := upper(btrim(p_metodo));
  recibido numeric(12,2);
  cambio_calculado numeric(12,2);
BEGIN
  IF NOT EXISTS (
    SELECT 1 FROM usuarios u
    JOIN usuario_roles ur ON ur.usuario_id = u.id
    JOIN roles r ON r.id = ur.rol_id
    WHERE u.id = p_cajero_id AND u.activo AND r.clave IN ('caja', 'administrador')
  ) THEN
    RAISE EXCEPTION 'El usuario no tiene permisos de caja.' USING ERRCODE = 'insufficient_privilege';
  END IF;

  IF NOT EXISTS (SELECT 1 FROM metodos_pago WHERE clave = metodo_normalizado AND activo) THEN
    RAISE EXCEPTION 'Método de pago inválido.';
  END IF;

  SELECT * INTO pedido_registro FROM pedidos WHERE id = p_pedido_id FOR UPDATE;
  IF NOT FOUND THEN RAISE EXCEPTION 'El pedido % no existe.', p_pedido_id; END IF;
  IF pedido_registro.estado <> 'ENTREGADO' THEN
    RAISE EXCEPTION 'El pedido debe estar ENTREGADO antes de cobrar. Estado actual: %', pedido_registro.estado;
  END IF;
  IF pedido_registro.total <= 0 THEN RAISE EXCEPTION 'El pedido no tiene un total válido.'; END IF;

  recibido := CASE
    WHEN metodo_normalizado = 'EFECTIVO' THEN p_monto_recibido
    ELSE pedido_registro.total
  END;
  IF recibido IS NULL OR recibido < pedido_registro.total THEN
    RAISE EXCEPTION 'El monto recibido es insuficiente.' USING ERRCODE = 'check_violation';
  END IF;
  cambio_calculado := CASE
    WHEN metodo_normalizado = 'EFECTIVO' THEN recibido - pedido_registro.total
    ELSE 0
  END;

  INSERT INTO pagos(pedido_id, cajero_id, metodo, total, monto_recibido, cambio, referencia)
  VALUES (
    pedido_registro.id, p_cajero_id, metodo_normalizado, pedido_registro.total,
    recibido, cambio_calculado, NULLIF(btrim(p_referencia), '')
  ) RETURNING id INTO pago_id_nuevo;

  INSERT INTO tickets(pago_id) VALUES (pago_id_nuevo)
  RETURNING id, tickets.folio INTO ticket_id_nuevo, folio_nuevo;

  UPDATE pedidos SET estado = 'PAGADO', actualizado_por = p_cajero_id
  WHERE id = pedido_registro.id;

  RETURN QUERY SELECT ticket_id_nuevo, folio_nuevo, pedido_registro.total, cambio_calculado;
END;
$$;

CREATE OR REPLACE VIEW vista_pedidos_operativos AS
SELECT
  p.id,
  m.numero AS mesa,
  u.nombre AS mesero,
  p.estado,
  p.subtotal,
  p.impuesto,
  p.total,
  p.creado_en,
  COALESCE(sum(pd.cantidad), 0)::integer AS cantidad_productos,
  COALESCE(
    jsonb_agg(jsonb_build_object(
      'producto_id', pd.producto_id,
      'nombre', pd.nombre_producto,
      'cantidad', pd.cantidad,
      'precio', pd.precio_unitario,
      'importe', pd.importe,
      'observacion', pd.observacion
    ) ORDER BY pd.id) FILTER (WHERE pd.id IS NOT NULL),
    '[]'::jsonb
  ) AS items
FROM pedidos p
JOIN mesas m ON m.id = p.mesa_id
JOIN usuarios u ON u.id = p.mesero_id
LEFT JOIN pedido_detalles pd ON pd.pedido_id = p.id
GROUP BY p.id, m.numero, u.nombre;

CREATE OR REPLACE VIEW vista_tickets AS
SELECT
  t.id,
  t.folio,
  t.emitido_en,
  p.id AS pedido_id,
  m.numero AS mesa,
  pg.metodo,
  pg.total,
  pg.monto_recibido,
  pg.cambio,
  c.nombre AS cajero,
  COALESCE(
    jsonb_agg(jsonb_build_object(
      'nombre', pd.nombre_producto,
      'cantidad', pd.cantidad,
      'precio', pd.precio_unitario,
      'importe', pd.importe
    ) ORDER BY pd.id) FILTER (WHERE pd.id IS NOT NULL),
    '[]'::jsonb
  ) AS items
FROM tickets t
JOIN pagos pg ON pg.id = t.pago_id
JOIN pedidos p ON p.id = pg.pedido_id
JOIN mesas m ON m.id = p.mesa_id
JOIN usuarios c ON c.id = pg.cajero_id
LEFT JOIN pedido_detalles pd ON pd.pedido_id = p.id
GROUP BY t.id, p.id, m.numero, pg.id, c.nombre;

CREATE OR REPLACE VIEW vista_ventas_diarias AS
SELECT
  (pg.creado_en AT TIME ZONE 'America/Mexico_City')::date AS fecha,
  count(*)::integer AS pagos,
  sum(pg.total)::numeric(14,2) AS total,
  sum(pg.total) FILTER (WHERE pg.metodo = 'EFECTIVO')::numeric(14,2) AS efectivo,
  sum(pg.total) FILTER (WHERE pg.metodo = 'TARJETA')::numeric(14,2) AS tarjeta,
  sum(pg.total) FILTER (WHERE pg.metodo = 'TRANSFERENCIA')::numeric(14,2) AS transferencia
FROM pagos pg
GROUP BY (pg.creado_en AT TIME ZONE 'America/Mexico_City')::date;

COMMIT;
