CREATE OR REPLACE FUNCTION fn_actualizar_timestamp()
RETURNS trigger
LANGUAGE plpgsql
AS $$
BEGIN
  NEW.actualizado_en := now();
  RETURN NEW;
END;
$$;

CREATE OR REPLACE FUNCTION fn_validar_transicion_pedido()
RETURNS trigger
LANGUAGE plpgsql
AS $$
DECLARE
  transicion_valida boolean;
BEGIN
  IF NEW.estado = OLD.estado THEN
    RETURN NEW;
  END IF;

  transicion_valida := CASE OLD.estado
    WHEN 'PENDIENTE' THEN NEW.estado IN ('PREPARANDO', 'CANCELADO')
    WHEN 'PREPARANDO' THEN NEW.estado IN ('LISTO', 'CANCELADO')
    WHEN 'LISTO' THEN NEW.estado IN ('ENTREGADO', 'CANCELADO')
    WHEN 'ENTREGADO' THEN NEW.estado IN ('PAGADO', 'CANCELADO')
    ELSE false
  END;

  IF NOT transicion_valida THEN
    RAISE EXCEPTION 'Transición de pedido no permitida: % -> %', OLD.estado, NEW.estado
      USING ERRCODE = 'check_violation';
  END IF;

  NEW.version := OLD.version + 1;
  IF NEW.estado = 'ENTREGADO' THEN NEW.entregado_en := now(); END IF;
  IF NEW.estado IN ('PAGADO', 'CANCELADO') THEN NEW.cerrado_en := now(); END IF;
  RETURN NEW;
END;
$$;

CREATE OR REPLACE FUNCTION fn_historial_estado_pedido()
RETURNS trigger
LANGUAGE plpgsql
AS $$
BEGIN
  IF TG_OP = 'INSERT' OR NEW.estado IS DISTINCT FROM OLD.estado THEN
    INSERT INTO pedido_estados_historial (
      pedido_id, estado_anterior, estado_nuevo, usuario_id
    ) VALUES (
      NEW.id,
      CASE WHEN TG_OP = 'INSERT' THEN NULL ELSE OLD.estado END,
      NEW.estado,
      NEW.actualizado_por
    );
  END IF;
  RETURN NEW;
END;
$$;

CREATE OR REPLACE FUNCTION fn_sincronizar_estado_mesa()
RETURNS trigger
LANGUAGE plpgsql
AS $$
DECLARE
  mesa_anterior smallint;
  mesa_actual smallint;
BEGIN
  mesa_anterior := CASE WHEN TG_OP IN ('UPDATE', 'DELETE') THEN OLD.mesa_id END;
  mesa_actual := CASE WHEN TG_OP IN ('INSERT', 'UPDATE') THEN NEW.mesa_id END;

  IF mesa_anterior IS NOT NULL THEN
    UPDATE mesas
       SET estado = CASE WHEN EXISTS (
         SELECT 1 FROM pedidos p
          WHERE p.mesa_id = mesa_anterior
            AND p.estado IN ('PENDIENTE', 'PREPARANDO', 'LISTO', 'ENTREGADO')
       ) THEN 'OCUPADA' ELSE 'DISPONIBLE' END
     WHERE id = mesa_anterior AND estado <> 'FUERA_SERVICIO';
  END IF;

  IF mesa_actual IS NOT NULL THEN
    UPDATE mesas
       SET estado = CASE WHEN EXISTS (
         SELECT 1 FROM pedidos p
          WHERE p.mesa_id = mesa_actual
            AND p.estado IN ('PENDIENTE', 'PREPARANDO', 'LISTO', 'ENTREGADO')
       ) THEN 'OCUPADA' ELSE 'DISPONIBLE' END
     WHERE id = mesa_actual AND estado <> 'FUERA_SERVICIO';
  END IF;
  RETURN NULL;
END;
$$;

CREATE OR REPLACE FUNCTION fn_recalcular_totales_pedido()
RETURNS trigger
LANGUAGE plpgsql
AS $$
DECLARE
  pedido_afectado bigint;
  subtotal_calculado numeric(12,2);
BEGIN
  pedido_afectado := CASE WHEN TG_OP = 'DELETE' THEN OLD.pedido_id ELSE NEW.pedido_id END;
  SELECT COALESCE(sum(importe), 0) INTO subtotal_calculado
    FROM pedido_detalles WHERE pedido_id = pedido_afectado;

  UPDATE pedidos
     SET subtotal = subtotal_calculado,
         impuesto = round(subtotal_calculado * tasa_impuesto, 2),
         total = subtotal_calculado + round(subtotal_calculado * tasa_impuesto, 2)
   WHERE id = pedido_afectado;

  IF TG_OP = 'UPDATE' AND OLD.pedido_id <> NEW.pedido_id THEN
    SELECT COALESCE(sum(importe), 0) INTO subtotal_calculado
      FROM pedido_detalles WHERE pedido_id = OLD.pedido_id;
    UPDATE pedidos
       SET subtotal = subtotal_calculado,
           impuesto = round(subtotal_calculado * tasa_impuesto, 2),
           total = subtotal_calculado + round(subtotal_calculado * tasa_impuesto, 2)
     WHERE id = OLD.pedido_id;
  END IF;
  RETURN NULL;
END;
$$;

CREATE OR REPLACE FUNCTION fn_auditar()
RETURNS trigger
LANGUAGE plpgsql
AS $$
DECLARE
  id_registro bigint;
BEGIN
  id_registro := CASE
    WHEN TG_OP = 'DELETE' THEN (to_jsonb(OLD)->>'id')::bigint
    ELSE (to_jsonb(NEW)->>'id')::bigint
  END;
  INSERT INTO auditoria(tabla, registro_id, accion, datos_anteriores, datos_nuevos)
  VALUES (
    TG_TABLE_SCHEMA || '.' || TG_TABLE_NAME,
    id_registro,
    TG_OP,
    CASE WHEN TG_OP IN ('UPDATE', 'DELETE') THEN to_jsonb(OLD) END,
    CASE WHEN TG_OP IN ('INSERT', 'UPDATE') THEN to_jsonb(NEW) END
  );
  RETURN NULL;
END;
$$;

